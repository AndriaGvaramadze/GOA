let Coffe = prompt("Enter number from 1 to 3: ")

switch(Coffe){
    case "1":
        console.log("Americano");
        break;
    case "2":
        console.log("Espresso");
        break;
    case "3":
        console.log ("Capuccino");
        break;
    default:
        console.log("Please enter valid answer")
}